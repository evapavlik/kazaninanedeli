/** Vyhledání záznamů podle cyklu */
export function getEntriesByYear(year: "A" | "B" | "C"): LectionaryEntry[] {
  return LECTIONARY.filter((e) => e.year === year);
}

/** Vyhledání záznamu podle sundayId a cyklu */
export function getEntry(sundayId: string, year: "A" | "B" | "C"): LectionaryEntry | undefined {
  return LECTIONARY.find((e) => e.sundayId === sundayId && e.year === year);
}

/** Neděle v daném liturgickém období */
export function getEntriesBySeason(seasonId: string, year: "A" | "B" | "C"): LectionaryEntry[] {
  return LECTIONARY.filter((e) => e.seasonId === seasonId && e.year === year);
}
